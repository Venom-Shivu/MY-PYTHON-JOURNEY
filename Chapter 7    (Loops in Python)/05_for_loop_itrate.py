                        # ITERATING FOR LOOP


# FOR loop with Lists:
list = [1 ,12 ,24, 52, 65, 896, 78, 98, 123, 253, 45, 12]

for i in list:
    print(i)
    # This will print all the contents of the lists one by one

#FOR loop with Tuples:
tuple = (6, 45, 85, 75, 122, 235, 45, 65, 78)

for i in tuple:
    print(i)

#FOR loop with Strings:
string = "Shivansh"
for i in string:
    print(i)
# This will print all the characters of the name one by one