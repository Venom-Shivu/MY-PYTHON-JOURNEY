# An Optional else can be used with a for loop if the code is to be executed when the loops exhausts.

for i in range(8):
    print(i+1)
else:
    print("Range Printed") # This is printed when the loop exhausts.

my_list = [1, 45, 78, 96, "Shivansh", "Divyansh", 45]

for items in my_list:
    print(items)
else:
    print("Done! List printed") # this statement will be printed when the loop exhausts.

