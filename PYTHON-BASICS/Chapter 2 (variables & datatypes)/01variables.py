# VARIABLE DECLARATION IN PYTHON
# -----------------------------------------
# A variable is a named reference to an object stored in memory.
# Python is dynamically typed, so the data type is inferred at runtime.

a = 1          # int → stores whole numbers
b = 2          # int → another integer value
c = 7.1        # float → accept decimal values
name = "shivu" # str → stores text (string)

# Performing an arithmetic operation using variables
print(a + b)   # Output: 3
print(c - a)   
print(c * b)   
print(c / b)   
print(c % b)
