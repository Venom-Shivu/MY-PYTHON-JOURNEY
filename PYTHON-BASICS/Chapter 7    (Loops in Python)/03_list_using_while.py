# Write a program to print the content of a list using while Loop.

my_list = [10, 11, True, "Shivansh", "Shivam", "Rahul", 29, False, "Abhinav", "Sakshi"]

i = 0

while( i < len (my_list)):
    print(my_list[i])
    i = i+1

# At very first i = 0 and length of list is 10.
# Then i = 1, it print 10 and so on.. till (length-1) as i startng from 0.
# When the value becomes 10<10 it stops the loop.
 