# The while loop is used to repeatedly execute a block of code as long as a specified condition is true.

i =1
while (i<6):
    print(i)
    i +=1 # It repetedly checks the condition increasing the value of i by 1 until the condition becomes false.

    """
    Output: 
    1 
    2 
    3 
    4 
    5 ( each number in a new line )
    
    Note: If we forget to increment the value of i, the condition will always be true and the loop will run indefinitely,
    without stopping. This is known as an infinite Loop.

    """
# Program to write from 1 to 50 using While Loop

k = 1
while (k <= 50):
    print(k, end = " ") # This will print the numbers in a single line seperated by space.
    k = k + 1 # Both are same
   



