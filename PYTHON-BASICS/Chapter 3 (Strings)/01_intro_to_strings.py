#Strings can be single quoted('shivu'), double quoted("shivu") or triple quoted 
# i.e multi line string ('''shivu''')
name = "Shivu"
#SLICING
nameshort = name[0:3] # start from 0 all the way till 3 (excluding 3)

print(nameshort) # prints 'shi' indexed from 0 to 2 excludes index 3
character1 = name[1] # Prints character at index 1, i.e 'h'
print(character1)                             

