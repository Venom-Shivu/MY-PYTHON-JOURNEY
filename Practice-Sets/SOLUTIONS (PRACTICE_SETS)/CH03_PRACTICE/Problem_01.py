#Write a python program to display a user entered name followed by Good Afternoon using input() function.

name = input("Enter your name: ")

print(f"Good Afternoon! {name}")
# 'f' before the string tells Python to evaluate the expressions or variables   inside the curly braces {}.
# Example Output:
# Enter your name: Shivansh Yadav  
# Good Afternoon! Shivansh Yadav

