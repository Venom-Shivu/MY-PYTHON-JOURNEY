# A module is a file containing Python code (functions, variables).
# You use the 'import' keyword to bring that code into your script.

import math  # Importing the built-in 'math' module

# Example: Using the 'pi' constant and 'sqrt' function from the math module
radius = 5
area = math.pi * (radius ** 2)
print(f"The area of a circle with radius {radius} is: {area:.2f}")