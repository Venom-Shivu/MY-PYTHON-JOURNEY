# ==========================================
#       PIP (Package Installer for Python)
# ==========================================
# 'pip' is the standard tool for installing external libraries 
# created by the community (like data science or web tools).

# IMPORTANT: You run pip commands in your Terminal/Command Prompt, 
# NOT inside your Python code.
# In VS code press "CTRL + ~" to open terminal

# --- Terminal Command Examples ---
# To install a library:      pip install requests
# To uninstall a library:    pip uninstall requests
# To see installed libs:     pip list

# Once installed via terminal, you can import them in your code:
# import requests
# response = requests.get('https://www.google.com')